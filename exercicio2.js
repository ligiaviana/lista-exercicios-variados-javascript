// 2) Escreva uma função que receba a idade de uma pessoa em anos e retorne a mesma idade em dias.

function converterIdadeEmAnosParaDias(idadeEmAnos) {
    const diasDoAno = 365
    return diasDoAno * idadeEmAnos
}
console.log(converterIdadeEmAnosParaDias(1))
console.log(converterIdadeEmAnosParaDias(35))
console.log(converterIdadeEmAnosParaDias(37))
console.log(converterIdadeEmAnosParaDias(84))





